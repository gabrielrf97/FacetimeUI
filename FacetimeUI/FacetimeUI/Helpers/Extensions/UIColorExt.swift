import Foundation
import UIKit

extension UIColor {
    class var appDarkGray: UIColor {
        return UIColor(red: 31.0 / 255.0, green: 31.0 / 255.0, blue: 31.0 / 255.0, alpha: 1.0)
    }
    
    class var appLightGray: UIColor {
        return UIColor(red: 60.0 / 255.0, green: 60.0 / 255.0, blue: 60.0 / 255.0, alpha: 1.0)
    }
    
    class var appWhite: UIColor {
        return UIColor(red: 254 / 255.0, green: 254 / 255.0, blue: 254 / 255.0, alpha: 1.0)
    }
    
    class var appBlack: UIColor {
        return UIColor(red: 3 / 255.0, green: 1 / 255.0, blue: 1 / 255.0, alpha: 1.0)
    }
    
    class var appRed: UIColor {
        return UIColor(red: 235 / 255.0, green: 85 / 255.0, blue: 69 / 255.0, alpha: 1.0)
    }
}
